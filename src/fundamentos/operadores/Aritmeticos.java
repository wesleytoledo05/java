package fundamentos.operadores;

public class Aritmeticos {
    public static void main(String[] args) {
        
        var x = 35;
        double y = 2;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y + "\n");
        
        int a = 8;
        int b = 2;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
    }
    
}
